async function refreshDashboard() {
  if (state.userRole !== "warden") {
    closeDashboardEvents();
    return;
  }

  try {
    const payload = await fetchJson("/api/dashboard");
    startPhoneServerRefresh();
    setAttendanceStateFromPayload({ modeActive: payload.attendanceModeActive, attendanceDateKey: payload.attendanceDateKey });
    state.dashboardRooms = applyAttendanceLocalOverrides(payload.rooms || []);
    renderDashboardRefreshStatus(payload.generatedAt);
    state.dashboardAttendanceDateKey = payload.attendanceDateKey || "";
    state.phoneLogs = Array.isArray(payload.phoneLogs) ? payload.phoneLogs : [];
    state.outingLogs = Array.isArray(payload.outingLogs) ? payload.outingLogs : [];
    state.overnightLogs = Array.isArray(payload.overnightLogs) ? payload.overnightLogs : [];
    state.deviceLogs = Array.isArray(payload.deviceLogs) ? payload.deviceLogs : [];
    syncSelectedPhoneStateFromDashboard(payload.users || []);
    updateDashboardFilterButtons();
    renderDashboardSummary();
    renderDashboardBoard();
    renderPhoneLogs();
    renderOfflineChangePanel();
    refreshPhoneTimers();
  } catch (error) {
    setMessage(error.message, true);
  }
}

function syncSelectedPhoneStateFromDashboard(users) {
  const selectedName = state.selectedName || getSelectedNameInput();
  if (!selectedName || !Array.isArray(users)) {
    return;
  }

  const selectedUser = users.find((user) => user && user.name === selectedName);
  if (!selectedUser) {
    return;
  }

  setPhoneSubmittedInState(selectedUser.phoneSubmitted);
  setPhoneTakenAtInState(getPhoneTakenAtForDisplay(selectedUser.name, selectedUser.phoneSubmitted, selectedUser.phoneTakenAt));
  setPhoneConfiscationInState(selectedUser.phoneConfiscation);
  setOutingActiveInState(selectedUser.outingActive);
  setOutingStartedAtInState(getOutingStartedAtForDisplay(selectedUser.name, selectedUser.outingActive, selectedUser.outingStartedAt));
  if (selectedUser.name === state.loginName) {
    setAttendanceStateFromPayload({
      checked: selectedUser.attendanceChecked,
      excluded: selectedUser.isOut || selectedUser.isOvernight,
    });
  }
  renderPhonePanel();
}

