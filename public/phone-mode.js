function canUsePhoneConfiscation() {
  return Boolean(state.authenticated && state.userRole === "warden");
}

function openPhoneConfiscationModal(name) {
  const targetName = String(name || "").trim();
  const modal = document.querySelector("#phoneConfiscationModal");
  const target = document.querySelector("#phoneConfiscationTarget");
  const daysInput = document.querySelector("#phoneConfiscationDaysInput");
  if (!targetName || !modal || !canUsePhoneConfiscation()) {
    closePhoneConfiscationModal();
    return;
  }

  const phoneConfiscation = normalizePhoneConfiscationInfo(getDashboardUserByName(targetName)?.phoneConfiscation);
  modal.dataset.phoneConfiscationTarget = targetName;
  if (target) {
    target.textContent = targetName;
  }
  if (daysInput) {
    daysInput.value = String(phoneConfiscation.active ? Math.max(1, phoneConfiscation.daysRemaining) : 0);
  }
  updatePhoneConfiscationModalEndPreview(phoneConfiscation);
  modal.classList.remove("hidden");
  daysInput?.focus();
}

function closePhoneConfiscationModal() {
  const modal = document.querySelector("#phoneConfiscationModal");
  if (!modal) {
    return;
  }

  modal.classList.add("hidden");
  modal.dataset.phoneConfiscationTarget = "";
  clearQueuedPhoneConfiscationModalSave();
}

function clearQueuedPhoneConfiscationModalSave() {
  if (!phoneConfiscationModalSaveTimerId) {
    return;
  }

  window.clearTimeout(phoneConfiscationModalSaveTimerId);
  phoneConfiscationModalSaveTimerId = null;
}

function queuePhoneConfiscationModalSave() {
  clearQueuedPhoneConfiscationModalSave();
  phoneConfiscationModalSaveTimerId = window.setTimeout(() => {
    phoneConfiscationModalSaveTimerId = null;
    savePhoneConfiscationFromModal({ silent: true });
  }, 500);
}

async function savePhoneConfiscationFromModal(options = {}) {
  const { silent = false } = options;
  if (!canUsePhoneConfiscation()) {
    closePhoneConfiscationModal();
    return;
  }

  const modal = document.querySelector("#phoneConfiscationModal");
  const daysInput = document.querySelector("#phoneConfiscationDaysInput");
  const targetName = String(modal?.dataset.phoneConfiscationTarget || "").trim();
  if (!targetName) {
    return;
  }

  const days = Number(daysInput?.value || 0);
  if (!Number.isInteger(days) || days < 0 || days > 365) {
    setMessage(PHONE_CONFISCATION_DAYS_ERROR_LABEL, true);
    return;
  }

  try {
    const payload = await fetchJson(`/api/admin/phone-confiscations/${encodeURIComponent(targetName)}`, {
      method: "PUT",
      headers: {
        "Content-Type": "application/json",
      },
      body: JSON.stringify(days === 0 ? { clear: true, days: 0 } : { days }),
    });
    const dashboardUser = getDashboardUserByName(payload.name || targetName);
    if (dashboardUser) {
      dashboardUser.phoneConfiscation = payload.phoneConfiscation;
    }
    const phoneConfiscation = normalizePhoneConfiscationInfo(payload.phoneConfiscation);
    updatePhoneConfiscationModalEndPreview(phoneConfiscation);
    renderPhoneLogs();
    renderOfflineChangePanel();
    if (!silent) {
      clearMessage();
    }
  } catch (error) {
    setMessage(error.message, true);
  }
}

