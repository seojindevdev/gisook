function matchesDashboardFilter(occupant) {
  if (!occupant || occupant.empty) {
    return false;
  }

  if (!matchesDashboardSearch(occupant)) {
    return false;
  }

  if (state.dashboardAttendanceMode) {
    return matchesDashboardGradeFilter(occupant) && !isAttendanceExcludedForOccupant(occupant);
  }

  if (["ALL", "GRADE_1", "GRADE_2", "GRADE_3"].includes(state.dashboardFilter)) {
    return matchesDashboardGradeFilter(occupant);
  }

  if (state.dashboardFilter === "PHONE") {
    if (!PHONE_BORROWING_ENABLED) {
      return false;
    }
    return occupant.phoneSubmitted === false;
  }

  if (state.dashboardFilter === "OUT") {
    return Boolean(occupant.isOut || occupant.outingActive);
  }

  return true;
}

function matchesDashboardGradeFilter(occupant) {
  if (state.dashboardFilter === "ALL") {
    return true;
  }

  if (state.dashboardFilter === "GRADE_1") {
    return occupant.grade === 1;
  }

  if (state.dashboardFilter === "GRADE_2") {
    return occupant.grade === 2;
  }

  if (state.dashboardFilter === "GRADE_3") {
    return occupant.grade === 3;
  }

  return false;
}

