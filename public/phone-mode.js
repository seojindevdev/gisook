function getAttendanceLocalOverrides() {
  if (
    !state.attendanceLocalOverrides ||
    typeof state.attendanceLocalOverrides !== "object" ||
    Array.isArray(state.attendanceLocalOverrides)
  ) {
    state.attendanceLocalOverrides = {};
  }

  return state.attendanceLocalOverrides;
}

function clearAttendanceLocalOverride(name) {
  const targetName = String(name || "").trim();
  if (!targetName) {
    return;
  }

  const overrides = getAttendanceLocalOverrides();
  delete overrides[targetName];
}

function applyAttendanceLocalOverrides(rooms) {
  const overrides = getAttendanceLocalOverrides();
  return (Array.isArray(rooms) ? rooms : []).map((room) => ({
    ...room,
    occupants: (Array.isArray(room.occupants) ? room.occupants : []).map((occupant) => {
      if (!occupant || occupant.empty || !Object.prototype.hasOwnProperty.call(overrides, occupant.name)) {
        return occupant;
      }

      return {
        ...occupant,
        attendanceChecked: !isAttendanceExcludedForOccupant(occupant) && Boolean(overrides[occupant.name]),
      };
    }),
  }));
}

function setAttendanceCheckedInDashboard(name, checked) {
  const targetName = String(name || "").trim();
  getAttendanceLocalOverrides()[targetName] = Boolean(checked);
  state.dashboardRooms = state.dashboardRooms.map((room) => ({
    ...room,
    occupants: (Array.isArray(room.occupants) ? room.occupants : []).map((occupant) =>
      occupant && !occupant.empty && occupant.name === targetName
        ? {
            ...occupant,
            attendanceChecked: Boolean(checked),
          }
        : occupant,
    ),
  }));
}

