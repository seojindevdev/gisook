function readSavedLoginPasswords() {
  try {
    const parsed = JSON.parse(localStorage.getItem(SAVED_LOGIN_PASSWORDS_KEY) || "{}");
    return parsed && typeof parsed === "object" && !Array.isArray(parsed) ? parsed : {};
  } catch (_error) {
    return {};
  }
}

function getSavedLoginPassword(name) {
  const targetName = String(name || "").trim();
  if (!targetName) {
    return "";
  }

  const passwords = readSavedLoginPasswords();
  return typeof passwords[targetName] === "string" ? passwords[targetName] : "";
}

function saveLoginPassword(name, password) {
  const targetName = String(name || "").trim();
  if (!targetName || !password) {
    return;
  }

  const passwords = readSavedLoginPasswords();
  passwords[targetName] = String(password);
  localStorage.setItem(SAVED_LOGIN_PASSWORDS_KEY, JSON.stringify(passwords));
}

function removeSavedLoginPassword(name) {
  const targetName = String(name || "").trim();
  if (!targetName) {
    return;
  }

  const passwords = readSavedLoginPasswords();
  if (!Object.prototype.hasOwnProperty.call(passwords, targetName)) {
    return;
  }

  delete passwords[targetName];
  localStorage.setItem(SAVED_LOGIN_PASSWORDS_KEY, JSON.stringify(passwords));
}

async function attemptSavedLoginForName(name, originalLogin) {
  const targetName = String(name || "").trim();
  const savedPassword = getSavedLoginPassword(targetName);
  if (!targetName || !savedPassword || storedLoginAutoInProgress || state.authenticated || typeof originalLogin !== "function") {
    return;
  }

  storedLoginAutoInProgress = true;
  try {
    elements.authNameInput.value = targetName;
    elements.passwordInput.value = savedPassword;
    await originalLogin();
    if (!state.authenticated) {
      removeSavedLoginPassword(targetName);
    }
  } finally {
    storedLoginAutoInProgress = false;
  }
}

function queueSavedLoginAttempt(originalLogin, explicitName = "") {
  window.setTimeout(() => {
    if (skipNextStoredLoginAttempt) {
      skipNextStoredLoginAttempt = false;
      return;
    }

    const selectedName = String(explicitName || localStorage.getItem("authSelectedName") || elements.authNameInput?.value || "").trim();
    attemptSavedLoginForName(selectedName, originalLogin);
  }, 60);
}

function setupSavedLoginOverrides() {
  const originalLogin = typeof login === "function" ? login : null;
  const originalLogout = typeof logout === "function" ? logout : null;
  const originalShowAuthGate = typeof showAuthGate === "function" ? showAuthGate : null;
  const originalSelectAuthName = typeof selectAuthName === "function" ? selectAuthName : null;
  const originalFinishLogin = typeof finishLogin === "function" ? finishLogin : null;
  if (!originalLogin || setupSavedLoginOverrides.bound) {
    return;
  }

  setupSavedLoginOverrides.bound = true;

  login = async function () {
    const name = String(elements.authNameInput?.value || "").trim();
    const password = String(elements.passwordInput?.value || "");
    await originalLogin();
    if (state.authenticated && name && password) {
      saveLoginPassword(name, password);
    }
  };

  if (originalLogout) {
    logout = async function () {
      skipNextStoredLoginAttempt = true;
      clearProtectedTabPinApiAccess();
      closeDashboardEvents();
      closeAttendanceEvents();
      await originalLogout();
    };
  }

  if (originalShowAuthGate) {
    showAuthGate = function () {
      clearProtectedTabPinApiAccess();
      closeDashboardEvents();
      closeAttendanceEvents();
      originalShowAuthGate();
      queueSavedLoginAttempt(originalLogin);
    };
  }

  if (originalFinishLogin) {
    finishLogin = async function (...args) {
      await originalFinishLogin(...args);
      startAttendanceServerRefresh();
      await refreshAttendanceStatus();
    };
  }

  if (originalSelectAuthName) {
    selectAuthName = function (name) {
      originalSelectAuthName(name);
      queueSavedLoginAttempt(originalLogin, name);
    };
  }

  document.addEventListener("DOMContentLoaded", () => {
    elements.authNameInput?.addEventListener("change", () => {
      const name = String(elements.authNameInput?.value || "").trim();
      if (name) {
        localStorage.setItem("authSelectedName", name);
        queueSavedLoginAttempt(originalLogin, name);
      }
    });
  });
}

