function normalizePhoneConfiscationInfo(value) {
  if (!value || typeof value !== "object" || Array.isArray(value) || !value.active) {
    return {
      active: false,
      untilDate: "",
      availableDate: "",
      availableAt: null,
      availableLabel: "",
      days: 0,
      daysRemaining: 0,
      startedAt: null,
      createdAt: null,
    };
  }

  const daysRemaining = Math.max(0, Number(value.daysRemaining ?? value.days ?? 0));
  const availableDate = String(value.availableDate || value.untilDate || "").trim();
  const availableLabel = String(value.availableLabel || availableDate).trim();
  return {
    active: Boolean(daysRemaining > 0),
    untilDate: availableDate,
    availableDate,
    availableAt: typeof value.availableAt === "string" ? value.availableAt : null,
    availableLabel,
    days: Math.max(0, Number(value.days || daysRemaining)),
    daysRemaining,
    startedAt: typeof value.startedAt === "string" ? value.startedAt : null,
    createdAt: typeof value.createdAt === "string" ? value.createdAt : null,
  };
}

