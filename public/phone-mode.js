async function verifyProtectedTabPin(pin, remember) {
  return fetchJson(
    "/api/auth/protected-tab-pin",
    {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
      },
      body: JSON.stringify({ pin, remember: Boolean(remember) }),
    },
    { allowUnauthorized: true },
  );
}

async function isProtectedTabPinRemembered() {
  try {
    const payload = await fetchJson("/api/auth/protected-tab-pin/status", {}, { allowUnauthorized: true });
    return Boolean(payload.remembered);
  } catch (_error) {
    return false;
  }
}

async function handleProtectedTabPinSubmit(event) {
  const form = event.target?.closest?.("[data-protected-tab-pin-form]");
  if (!form) {
    return;
  }

  event.preventDefault();
  const input = document.querySelector("#protectedTabPinInput");
  const rememberInput = document.querySelector("#protectedTabPinRememberInput");
  const message = document.querySelector("#protectedTabPinMessage");
  const pin = String(input?.value || "").trim();
  try {
    const payload = await verifyProtectedTabPin(pin, Boolean(rememberInput?.checked));
    protectedTabPinApiToken = String(payload?.token || "");
  } catch (_error) {
    if (message) {
      message.textContent = PROTECTED_TAB_PIN_ERROR_LABEL;
      message.classList.remove("hidden");
    }
    input?.focus();
    input?.select();
    return;
  }

  protectedTabPinSessionUnlocked = true;
  completeProtectedTabPinAccess();
}

