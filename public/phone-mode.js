function normalizeRequestedStatusReason(value) {
  return String(value || "")
    .trim()
    .replace(/\s+/g, " ");
}

function setStatusReasonModalMessage(text = "") {
  const { message } = getStatusReasonModalElements();
  if (!message) {
    return;
  }

  message.textContent = text;
  message.classList.toggle("hidden", !text);
}

function closeStatusReasonModal(value = null) {
  const { modal, input } = getStatusReasonModalElements();
  modal?.classList.add("hidden");
  if (input) {
    input.value = "";
  }
  setStatusReasonModalMessage("");

  if (statusReasonModalResolver) {
    const resolver = statusReasonModalResolver;
    statusReasonModalResolver = null;
    resolver(value);
  }
}

function validateStatusReasonModalInput() {
  const { input } = getStatusReasonModalElements();
  const reason = normalizeRequestedStatusReason(input?.value);
  if (!reason) {
    setStatusReasonModalMessage(STATUS_REASON_REQUIRED_LABEL);
    setMessage(STATUS_REASON_REQUIRED_LABEL, true);
    input?.focus();
    return null;
  }

  if (reason.length > 100) {
    setStatusReasonModalMessage(STATUS_REASON_LENGTH_ERROR_LABEL);
    setMessage(STATUS_REASON_LENGTH_ERROR_LABEL, true);
    input?.focus();
    return null;
  }

  return reason;
}

function requestStatusChangeReason(type) {
  if (!PHONE_BORROWING_ENABLED && type === "phone") {
    return Promise.resolve(null);
  }

  ensureStatusReasonModal();
  if (statusReasonModalResolver) {
    closeStatusReasonModal(null);
  }

  const { modal, title, prompt, input } = getStatusReasonModalElements();
  if (!modal || !input) {
    return Promise.resolve(null);
  }

  const isPhone = type === "phone";
  if (title) {
    title.textContent = isPhone ? PHONE_BORROW_ACTION_LABEL : OUTING_LEAVE_ACTION_LABEL;
  }
  if (prompt) {
    prompt.textContent = isPhone ? PHONE_REASON_PROMPT_LABEL : OUTING_REASON_PROMPT_LABEL;
  }
  input.value = "";
  setStatusReasonModalMessage("");
  modal.classList.remove("hidden");
  window.setTimeout(() => input.focus(), 0);

  return new Promise((resolve) => {
    statusReasonModalResolver = resolve;
  });
}

function handleStatusReasonModalClick(event) {
  const { modal } = getStatusReasonModalElements();
  if (!modal || modal.classList.contains("hidden")) {
    return;
  }

  if (event.target === modal || event.target.closest("[data-status-reason-cancel]")) {
    closeStatusReasonModal(null);
  }
}

function handleStatusReasonModalInput(event) {
  if (event.target?.id !== "statusReasonInput") {
    return;
  }

  setStatusReasonModalMessage("");
}

function handleStatusReasonModalSubmit(event) {
  const form = event.target?.closest?.("[data-status-reason-form]");
  if (!form) {
    return;
  }

  event.preventDefault();
  const reason = validateStatusReasonModalInput();
  if (reason === null) {
    return;
  }

  clearMessage();
  closeStatusReasonModal(reason);
}

async function loadUser(name) {
  const trimmedName = String(name || "").trim();
  if (!trimmedName) {
    state.selectedName = "";
    setPhoneSubmittedInState(PHONE_DEFAULT_SUBMITTED);
    setPhoneTakenAtInState(null);
    setPhoneConfiscationInState(null);
    setOutingActiveInState(OUTING_DEFAULT_ACTIVE);
    setOutingStartedAtInState(null);
    renderPhonePanel();
    return;
  }

  cancelAutoSave();

  try {
    const payload = await fetchJson(`/api/users/${encodeURIComponent(trimmedName)}`);
    state.selectedName = payload.name;
    state.selectedRoom = payload.room || "";
    state.selectedSlot = Number(payload.slot || 0);
    state.intervals = [];
    state.overnights = [];
    setPhoneSubmittedInState(payload.phoneSubmitted);
    setPhoneTakenAtInState(getPhoneTakenAtForDisplay(payload.name, payload.phoneSubmitted, payload.phoneTakenAt));
    setPhoneConfiscationInState(payload.phoneConfiscation);
    setOutingActiveInState(payload.outingActive);
    setOutingStartedAtInState(getOutingStartedAtForDisplay(payload.name, payload.outingActive, payload.outingStartedAt));
    setAttendanceStateFromPayload({
      modeActive: payload.attendanceModeActive,
      attendanceDateKey: payload.attendanceDateKey,
      checked: payload.attendanceChecked,
      excluded: payload.status?.isOut || payload.status?.isOvernight,
    });
    state.hasUnsavedChanges = false;

    elements.nameInput.value = payload.name;
    localStorage.setItem("selectedName", payload.name);

    renderScheduleRows();
    renderOvernightRows();
    renderChart();
    renderPhonePanel();
    clearMessage();
  } catch (error) {
    setMessage(error.message, true);
  }
}

async function saveUser(options = {}) {
  const { isAuto = false, keepalive = false } = options;
  const name = state.selectedName || getSelectedNameInput();
  if (!name) {
    return;
  }

  try {
    const payload = await fetchJson(`/api/users/${encodeURIComponent(name)}`, {
      method: "PUT",
      headers: {
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        phoneSubmitted: getPhoneSubmittedFromState(),
        outingActive: getOutingActiveFromState(),
      }),
      keepalive,
    });

    state.selectedName = payload.name;
    setPhoneSubmittedInState(payload.phoneSubmitted);
    setPhoneTakenAtInState(getPhoneTakenAtForDisplay(payload.name, payload.phoneSubmitted, payload.phoneTakenAt));
    setPhoneConfiscationInState(payload.phoneConfiscation);
    setOutingActiveInState(payload.outingActive);
    setOutingStartedAtInState(getOutingStartedAtForDisplay(payload.name, payload.outingActive, payload.outingStartedAt));
    state.hasUnsavedChanges = false;
    renderPhonePanel();

    if (!isAuto) {
      await refreshDashboard();
    }
  } catch (error) {
    setMessage(error.message, true);
  }
}

