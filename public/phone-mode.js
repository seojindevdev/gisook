function getLogDateKey(log) {
  const value =
    activeHistoryLogType === "overnight"
      ? log?.createdAt
      : activeHistoryLogType === "device"
        ? log?.lastSeenAt || log?.firstSeenAt
        : log?.endAt || log?.startAt;
  return getSeoulDateKey(value);
}

function matchesPhoneLogDateFilter(log) {
  if (phoneLogDateFilter === "ALL") {
    return true;
  }

  const logDateKey = getLogDateKey(log);
  if (!logDateKey) {
    return false;
  }

  if (phoneLogDateFilter === "TODAY") {
    return logDateKey === getSeoulDateKey();
  }

  if (phoneLogDateFilter === "WEEK") {
    const { start, end } = getCurrentWeekRangeForUi();
    return logDateKey >= start && logDateKey <= end;
  }

  return true;
}

function formatPhoneLogDuration(durationSeconds) {
  const totalSeconds = Math.max(0, Number(durationSeconds || 0));
  if (totalSeconds < 60) {
    return LESS_THAN_ONE_MINUTE_LABEL;
  }

  const totalMinutes = Math.max(1, Math.round(totalSeconds / 60));
  const hours = Math.floor(totalMinutes / 60);
  const minutes = totalMinutes % 60;
  if (hours > 0 && minutes > 0) {
    return `${hours}${HOUR_LABEL} ${minutes}${MINUTE_LABEL}`;
  }

  if (hours > 0) {
    return `${hours}${HOUR_LABEL}`;
  }

  return `${totalMinutes}${MINUTE_LABEL}`;
}

function formatOvernightLogDate(value) {
  const match = /^(\d{4})-(\d{2})-(\d{2})$/.exec(String(value || ""));
  if (!match) {
    return "";
  }

  return `${Number(match[2])}\uc6d4 ${Number(match[3])}\uc77c`;
}

function getOvernightLogActionText(action) {
  return String(action || "").toUpperCase() === "CANCEL" ? OVERNIGHT_LOG_CANCEL_LABEL : OVERNIGHT_LOG_SET_LABEL;
}

function getOvernightLogActionClass(action) {
  return String(action || "").toUpperCase() === "CANCEL" ? "is-cancel" : "is-set";
}

function getPhoneLogSearchQuery() {
  return String(document.querySelector("#phoneLogSearchInput")?.value || "").trim();
}

function getPhoneLogStudentMeta(name) {
  const targetName = String(name || "").trim();
  const dashboardUsers = Array.isArray(state.dashboardRooms)
    ? state.dashboardRooms.flatMap((room) => (Array.isArray(room.occupants) ? room.occupants : []))
    : [];
  const dashboardUser = dashboardUsers.find((user) => user && !user.empty && user.name === targetName);
  const rosterUser = Array.isArray(state.people) ? state.people.find((person) => person && person.name === targetName) : null;
  const grade = Number(dashboardUser?.grade ?? rosterUser?.grade);
  const room = String(dashboardUser?.room ?? rosterUser?.room ?? "").trim();

  return {
    grade: Number.isInteger(grade) ? grade : null,
    room,
  };
}

function getLogDurationSeconds(startAt, endAt = new Date().toISOString()) {
  const startMs = Date.parse(startAt || "");
  const endMs = Date.parse(endAt || "");
  if (Number.isNaN(startMs) || Number.isNaN(endMs)) {
    return 0;
  }

  return Math.max(0, Math.floor((endMs - startMs) / 1000));
}

function buildActivePhoneLog(user, nowIso) {
  const name = String(user?.name || "").trim();
  const startAt = getPhoneTakenAtForDisplay(name, Boolean(user?.phoneSubmitted), user?.phoneTakenAt);
  if (!name || user?.phoneSubmitted !== false || !startAt) {
    return null;
  }

  return {
    id: `active-phone-${name}`,
    name,
    startAt,
    endAt: nowIso,
    durationSeconds: getLogDurationSeconds(startAt, nowIso),
    reason: String(user?.phoneReason || "").trim(),
    active: true,
  };
}

function buildActiveOutingLog(user, nowIso) {
  const name = String(user?.name || "").trim();
  const outingActive = Boolean(user?.outingActive);
  const startAt = getOutingStartedAtForDisplay(name, outingActive, user?.outingStartedAt || user?.currentInterval?.startedAt);
  if (!name || !outingActive || !startAt || user?.isOvernight) {
    return null;
  }

  return {
    id: `active-outing-${name}`,
    name,
    startAt,
    endAt: nowIso,
    durationSeconds: getLogDurationSeconds(startAt, nowIso),
    reason: String(user?.outingReason || user?.currentInterval?.reason || "").trim(),
    active: true,
  };
}

function getActiveStatusLogs(type) {
  if (type !== "phone" && type !== "outing") {
    return [];
  }
  if (!PHONE_BORROWING_ENABLED && type === "phone") {
    return [];
  }

  const nowIso = new Date().toISOString();
  return getDashboardUsers()
    .map((user) => (type === "phone" ? buildActivePhoneLog(user, nowIso) : buildActiveOutingLog(user, nowIso)))
    .filter(Boolean);
}

function getHistoryLogSortTime(log) {
  const time = Date.parse(log?.endAt || log?.startAt || "");
  return Number.isNaN(time) ? 0 : time;
}

function compareHistoryLogs(left, right) {
  if (Boolean(left?.active) !== Boolean(right?.active)) {
    return left?.active ? -1 : 1;
  }

  return getHistoryLogSortTime(right) - getHistoryLogSortTime(left);
}

function getHistoryLogsForActiveType() {
  if (!PHONE_BORROWING_ENABLED && activeHistoryLogType === "phone") {
    activeHistoryLogType = "outing";
  }

  const storedLogs =
    activeHistoryLogType === "device"
      ? Array.isArray(state.deviceLogs)
        ? state.deviceLogs
        : []
      : activeHistoryLogType === "phone"
      ? Array.isArray(state.phoneLogs)
        ? state.phoneLogs
        : []
      : activeHistoryLogType === "overnight"
        ? Array.isArray(state.overnightLogs)
          ? state.overnightLogs
          : []
        : Array.isArray(state.outingLogs)
          ? state.outingLogs
          : [];

  if (activeHistoryLogType !== "phone" && activeHistoryLogType !== "outing") {
    return storedLogs;
  }

  return [...getActiveStatusLogs(activeHistoryLogType), ...storedLogs].sort(compareHistoryLogs);
}

function getFilteredPhoneLogs(logs) {
  const searchQuery = getPhoneLogSearchQuery();
  const gradeFilter = phoneLogGradeFilter;

  return logs.filter((log) => {
    const name = String(log.name || "");
    const meta = getPhoneLogStudentMeta(name);

    if (gradeFilter !== "ALL" && meta.grade !== Number(gradeFilter)) {
      return false;
    }

    if (searchQuery && !name.includes(searchQuery)) {
      return false;
    }

    if (!matchesPhoneLogDateFilter(log)) {
      return false;
    }

    return true;
  });
}

