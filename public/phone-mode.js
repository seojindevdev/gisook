function renderPhonePanel() {
  const panel = document.querySelector("#phonePanel");
  if (!panel) {
    return;
  }

  const label = document.querySelector("#phonePersonLabel");
  const timerLabel = document.querySelector("#phoneTimerLabel");
  const toggleButton = document.querySelector("#phoneToggleButton");
  const outingTimerLabel = document.querySelector("#outingTimerLabel");
  const outingToggleButton = document.querySelector("#outingToggleButton");
  const attendanceCard = document.querySelector("#attendanceStatusCard");
  const attendanceStatusLabel = document.querySelector("#attendanceStatusLabel");
  const attendanceToggleButton = document.querySelector("#attendanceToggleButton");
  const phoneSubmitted = getPhoneSubmittedFromState();
  const outingActive = getOutingActiveFromState();
  const selectedName = state.selectedName || getSelectedNameInput();
  const phoneTakenAt = getPhoneTakenAtForDisplay(selectedName, phoneSubmitted, getPhoneTakenAtFromState());
  const outingStartedAt = getOutingStartedAtForDisplay(selectedName, outingActive, getOutingStartedAtFromState());
  if (phoneTakenAt !== getPhoneTakenAtFromState()) {
    setPhoneTakenAtInState(phoneTakenAt);
  }
  if (outingStartedAt !== getOutingStartedAtFromState()) {
    setOutingStartedAtInState(outingStartedAt);
  }
  const phoneActionLabel = phoneSubmitted ? PHONE_BORROW_ACTION_LABEL : PHONE_RETURN_ACTION_LABEL;
  const timerText = getPhoneTimerText(phoneTakenAt, phoneSubmitted);
  const outingActionLabel = outingActive ? OUTING_RETURN_ACTION_LABEL : OUTING_LEAVE_ACTION_LABEL;
  const outingTimerText = getOutingTimerText(outingStartedAt, outingActive);
  const phoneConfiscation = getPhoneConfiscationFromState();
  const canChangePhoneStatus = canChangePhoneForName(selectedName, !phoneSubmitted);
  const canChangeOutingStatus = canChangeOutingForName(selectedName);
  const isStudentSelf = state.userRole === "student" && selectedName && selectedName === state.loginName;
  const attendanceModeActive = getAttendanceModeActiveFromState();
  const attendanceChecked = Boolean(state.attendanceChecked);
  const attendanceExcluded = Boolean(state.attendanceExcluded || outingActive);
  const showAttendanceButton = Boolean(attendanceModeActive && isStudentSelf);
  const displayTimerText =
    phoneSubmitted && isPhoneConfiscated(phoneConfiscation) ? formatPhoneConfiscationBorrowableMessage(phoneConfiscation) : timerText;

  if (label) {
    label.textContent = selectedName ? selectedName : PHONE_EMPTY_STUDENT_LABEL;
  }

  if (timerLabel) {
    timerLabel.textContent = selectedName ? displayTimerText : "";
    timerLabel.setAttribute("aria-label", `${PHONE_TIMER_ARIA_LABEL} ${displayTimerText}`);
  }

  if (toggleButton) {
    toggleButton.disabled = !canChangePhoneStatus;
    toggleButton.textContent = phoneActionLabel;
    toggleButton.setAttribute("aria-label", selectedName ? `${selectedName} ${phoneActionLabel} ${displayTimerText}` : PHONE_EMPTY_STUDENT_LABEL);
    toggleButton.classList.toggle("is-submitted", phoneSubmitted);
    toggleButton.classList.toggle("is-taken", !phoneSubmitted);
  }

  if (outingTimerLabel) {
    outingTimerLabel.textContent = selectedName ? outingTimerText : "";
    outingTimerLabel.setAttribute("aria-label", `${OUTING_TIMER_ARIA_LABEL} ${outingTimerText}`);
  }

  if (outingToggleButton) {
    outingToggleButton.disabled = !canChangeOutingStatus;
    outingToggleButton.textContent = outingActionLabel;
    outingToggleButton.setAttribute("aria-label", selectedName ? `${selectedName} ${outingActionLabel} ${outingTimerText}` : PHONE_EMPTY_STUDENT_LABEL);
    outingToggleButton.classList.toggle("is-submitted", !outingActive);
    outingToggleButton.classList.toggle("is-taken", outingActive);
  }

  if (attendanceCard) {
    attendanceCard.classList.toggle("hidden", !showAttendanceButton);
  }
  if (attendanceStatusLabel) {
    attendanceStatusLabel.textContent = attendanceChecked
      ? ATTENDANCE_COMPLETE_LABEL
      : attendanceExcluded
        ? ATTENDANCE_UNAVAILABLE_LABEL
        : ATTENDANCE_ACTION_LABEL;
  }
  if (attendanceToggleButton) {
    attendanceToggleButton.disabled = !showAttendanceButton || attendanceChecked || attendanceExcluded;
    attendanceToggleButton.textContent = attendanceChecked ? ATTENDANCE_COMPLETE_LABEL : ATTENDANCE_ACTION_LABEL;
    attendanceToggleButton.classList.toggle("is-submitted", attendanceChecked);
    attendanceToggleButton.classList.toggle("is-taken", !attendanceChecked);
    attendanceToggleButton.setAttribute(
      "aria-label",
      selectedName ? `${selectedName} ${attendanceChecked ? ATTENDANCE_COMPLETE_LABEL : ATTENDANCE_ACTION_LABEL}` : PHONE_EMPTY_STUDENT_LABEL,
    );
  }
}

function canChangeOutingForName(name) {
  if (!name) {
    return false;
  }

  const status = getOfflineStudentStatus(name);
  if (status.isOvernight) {
    return false;
  }

  return state.userRole === "warden" || name === state.loginName;
}

function canChangePhoneForName(name, nextPhoneSubmitted = null) {
  if (!PHONE_BORROWING_ENABLED) {
    return false;
  }

  if (!canChangeOutingForName(name)) {
    return false;
  }

  const status = getOfflineStudentStatus(name);
  const willBorrowPhone = nextPhoneSubmitted === false || (nextPhoneSubmitted === null && status.phoneSubmitted);
  if (willBorrowPhone && status.phoneSubmitted && isPhoneConfiscated(status.phoneConfiscation)) {
    return false;
  }

  return true;
}

async function savePhoneStatusForName(name, phoneSubmitted, reason = "") {
  if (!PHONE_BORROWING_ENABLED) {
    return false;
  }

  const targetName = String(name || "").trim();
  if (!targetName) {
    return false;
  }

  if (!canChangePhoneForName(targetName, Boolean(phoneSubmitted))) {
    const status = getOfflineStudentStatus(targetName);
    if (status.phoneSubmitted && Boolean(phoneSubmitted) === false && isPhoneConfiscated(status.phoneConfiscation)) {
      setMessage(formatPhoneConfiscationBorrowableMessage(status.phoneConfiscation), true);
    }
    return false;
  }

  const payload = await fetchJson(`/api/users/${encodeURIComponent(targetName)}`, {
    method: "PUT",
    headers: {
      "Content-Type": "application/json",
    },
    body: JSON.stringify({
      phoneSubmitted: Boolean(phoneSubmitted),
      phoneReason: Boolean(phoneSubmitted) ? "" : reason,
    }),
  });

  if (targetName === state.selectedName || targetName === getSelectedNameInput()) {
    setPhoneSubmittedInState(payload.phoneSubmitted);
    setPhoneTakenAtInState(getPhoneTakenAtForDisplay(payload.name, payload.phoneSubmitted, payload.phoneTakenAt));
    setPhoneConfiscationInState(payload.phoneConfiscation);
    setOutingActiveInState(payload.outingActive);
    setOutingStartedAtInState(getOutingStartedAtForDisplay(payload.name, payload.outingActive, payload.outingStartedAt));
    state.selectedName = payload.name;
    elements.nameInput.value = payload.name;
    renderPhonePanel();
  }

  await refreshDashboard();
  refreshPhoneTimers();
  return true;
}

async function saveOutingStatusForName(name, outingActive, reason = "") {
  const targetName = String(name || "").trim();
  if (!targetName || !canChangeOutingForName(targetName)) {
    return false;
  }

  const payload = await fetchJson(`/api/users/${encodeURIComponent(targetName)}`, {
    method: "PUT",
    headers: {
      "Content-Type": "application/json",
    },
    body: JSON.stringify({
      outingActive: Boolean(outingActive),
      outingReason: Boolean(outingActive) ? reason : "",
    }),
  });

  if (targetName === state.selectedName || targetName === getSelectedNameInput()) {
    setPhoneSubmittedInState(payload.phoneSubmitted);
    setPhoneTakenAtInState(getPhoneTakenAtForDisplay(payload.name, payload.phoneSubmitted, payload.phoneTakenAt));
    setPhoneConfiscationInState(payload.phoneConfiscation);
    setOutingActiveInState(payload.outingActive);
    setOutingStartedAtInState(getOutingStartedAtForDisplay(payload.name, payload.outingActive, payload.outingStartedAt));
    state.selectedName = payload.name;
    elements.nameInput.value = payload.name;
    renderPhonePanel();
  }

  await refreshDashboard();
  refreshPhoneTimers();
  return true;
}

async function handlePhoneToggleClick(event) {
  const button = event.target.closest("[data-phone-toggle]");
  if (!button) {
    return;
  }
  if (!PHONE_BORROWING_ENABLED) {
    return;
  }

  const selectedName = state.selectedName || getSelectedNameInput();
  if (!selectedName) {
    return;
  }

  const previousPhoneSubmitted = getPhoneSubmittedFromState();
  const previousPhoneTakenAt = getPhoneTakenAtFromState();
  const nextPhoneSubmitted = !previousPhoneSubmitted;
  const reason = nextPhoneSubmitted ? "" : await requestStatusChangeReason("phone");
  if (reason === null) {
    return;
  }

  const optimisticPhoneTakenAt = nextPhoneSubmitted ? null : new Date().toISOString();
  setPhoneSubmittedInState(nextPhoneSubmitted);
  setPhoneTakenAtInState(optimisticPhoneTakenAt);
  renderPhonePanel();

  try {
    await savePhoneStatusForName(selectedName, nextPhoneSubmitted, reason);
  } catch (error) {
    setPhoneSubmittedInState(previousPhoneSubmitted);
    setPhoneTakenAtInState(previousPhoneTakenAt);
    renderPhonePanel();
    setMessage(error.message, true);
    await loadUser(selectedName);
  }
}

async function handleOutingToggleClick(event) {
  const button = event.target.closest("[data-outing-toggle]");
  if (!button) {
    return;
  }

  const selectedName = state.selectedName || getSelectedNameInput();
  if (!selectedName) {
    return;
  }

  const previousOutingActive = getOutingActiveFromState();
  const previousOutingStartedAt = getOutingStartedAtFromState();
  const nextOutingActive = !previousOutingActive;
  const reason = nextOutingActive ? await requestStatusChangeReason("outing") : "";
  if (reason === null) {
    return;
  }

  const optimisticOutingStartedAt = nextOutingActive ? new Date().toISOString() : null;
  setOutingActiveInState(nextOutingActive);
  setOutingStartedAtInState(optimisticOutingStartedAt);
  renderPhonePanel();

  try {
    await saveOutingStatusForName(selectedName, nextOutingActive, reason);
  } catch (error) {
    setOutingActiveInState(previousOutingActive);
    setOutingStartedAtInState(previousOutingStartedAt);
    renderPhonePanel();
    setMessage(error.message, true);
    await loadUser(selectedName);
  }
}

async function handleAttendanceToggleClick(event) {
  const button = event.target.closest("[data-attendance-toggle]");
  if (!button) {
    return;
  }

  if (state.userRole !== "student" || !getAttendanceModeActiveFromState() || state.attendanceChecked) {
    return;
  }

  button.disabled = true;
  try {
    const payload = await fetchJson("/api/attendance/toggle", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
      },
      body: JSON.stringify({}),
    });
    setAttendanceStateFromPayload({
      ...payload,
      checked: Boolean(payload.checked),
      modeActive: payload.modeActive ?? getAttendanceModeActiveFromState(),
    });
    renderPhonePanel();
  } catch (error) {
    setMessage(error.message, true);
    await refreshAttendanceStatus();
  } finally {
    button.disabled = Boolean(state.attendanceChecked);
  }
}

