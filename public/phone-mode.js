function formatStatusSummaryNames(users) {
  if (!users.length) {
    return "\uc5c6\uc74c";
  }

  const names = users.map((user) => String(user.name || "")).filter(Boolean);
  const visibleNames = names.slice(0, 6).join(", ");
  const remaining = Math.max(0, names.length - 6);
  return remaining > 0 ? `${visibleNames} \uc678 ${remaining}\uba85` : visibleNames;
}

