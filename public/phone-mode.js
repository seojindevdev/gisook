function getPhoneDurationParts(phoneTakenAt, nowMs = Date.now()) {
  const startMs = Date.parse(phoneTakenAt || "");
  if (Number.isNaN(startMs)) {
    return null;
  }

  const totalSeconds = Math.max(0, Math.floor((nowMs - startMs) / 1000));
  const days = Math.floor(totalSeconds / 86400);
  const hours = Math.floor((totalSeconds % 86400) / 3600);
  const minutes = Math.floor((totalSeconds % 3600) / 60);
  const seconds = totalSeconds % 60;

  return { days, hours, minutes, seconds };
}

function formatPhoneDuration(phoneTakenAt, phoneSubmitted = false) {
  if (phoneSubmitted) {
    return PHONE_SUBMITTED_DURATION_LABEL;
  }

  const parts = getPhoneDurationParts(phoneTakenAt);
  if (!parts) {
    return PHONE_ZERO_DURATION_LABEL;
  }

  if (parts.days > 0) {
    return `${parts.days}${DAY_LABEL} ${parts.hours}${HOUR_LABEL} ${parts.minutes}${MINUTE_LABEL}`;
  }

  if (parts.hours > 0) {
    return `${parts.hours}${HOUR_LABEL} ${parts.minutes}${MINUTE_LABEL} ${parts.seconds}${SECOND_LABEL}`;
  }

  return `${parts.minutes}${MINUTE_LABEL} ${parts.seconds}${SECOND_LABEL}`;
}

