function mergeOfflineStudentRecord(records, person, sortIndex) {
  const name = String(person?.name || "").trim();
  if (!name) {
    return;
  }

  const existing = records.get(name) || {};
  records.set(name, {
    ...existing,
    name,
    grade: existing.grade ?? person.grade,
    room: existing.room ?? person.room,
    slot: existing.slot ?? person.slot,
    sortIndex: existing.sortIndex ?? sortIndex,
  });
}

function getOfflineStudentRoster() {
  const records = new Map();
  const people = Array.isArray(state.people) ? state.people : [];
  const dashboardUsers = getDashboardUsers();

  people.forEach((person, index) => mergeOfflineStudentRecord(records, person, index));
  dashboardUsers.forEach((person, index) => mergeOfflineStudentRecord(records, person, people.length + index));

  return Array.from(records.values()).sort((left, right) => {
    const leftIndex = Number(left.sortIndex);
    const rightIndex = Number(right.sortIndex);
    if (Number.isFinite(leftIndex) && Number.isFinite(rightIndex) && leftIndex !== rightIndex) {
      return leftIndex - rightIndex;
    }

    return String(left.name || "").localeCompare(String(right.name || ""), "ko");
  });
}

function getOfflineStudentStatus(name) {
  const targetName = String(name || "").trim();
  const dashboardUser = getDashboardUsers().find((user) => user.name === targetName);
  const rosterUser = Array.isArray(state.people) ? state.people.find((person) => person && person.name === targetName) : null;
  const phoneSubmittedSource = dashboardUser?.phoneSubmitted ?? rosterUser?.phoneSubmitted;
  const outingActiveSource = dashboardUser?.outingActive ?? dashboardUser?.isOut ?? rosterUser?.outingActive;
  const phoneSubmitted = phoneSubmittedSource === undefined || phoneSubmittedSource === null ? PHONE_DEFAULT_SUBMITTED : Boolean(phoneSubmittedSource);
  const outingActive = outingActiveSource === undefined || outingActiveSource === null ? OUTING_DEFAULT_ACTIVE : Boolean(outingActiveSource);
  const phoneTakenAt = getPhoneTakenAtForDisplay(targetName, phoneSubmitted, dashboardUser?.phoneTakenAt ?? rosterUser?.phoneTakenAt ?? null);
  const phoneConfiscation = normalizePhoneConfiscationInfo(dashboardUser?.phoneConfiscation ?? rosterUser?.phoneConfiscation ?? null);
  const outingStartedAt = getOutingStartedAtForDisplay(targetName, outingActive, dashboardUser?.outingStartedAt ?? rosterUser?.outingStartedAt ?? null);
  const grade = Number(dashboardUser?.grade ?? rosterUser?.grade);
  const room = String(dashboardUser?.room ?? rosterUser?.room ?? "").trim();

  return {
    phoneSubmitted,
    phoneTakenAt,
    phoneConfiscation,
    outingActive,
    outingStartedAt,
    isOvernight: Boolean(dashboardUser?.isOvernight),
    grade: Number.isInteger(grade) ? grade : null,
    room,
  };
}

function getFilteredOfflineStudents() {
  const query = getOfflineSearchQuery();
  if (!query) {
    return [];
  }

  return getOfflineStudentRoster().filter((person) => String(person.name || "").includes(query));
}

function renderOfflineChangePanel() {
  const panel = document.querySelector("#offlineChangePanel");
  const rows = document.querySelector("#offlineStudentResults");
  const count = document.querySelector("#offlineStudentCount");
  if (!panel || !rows) {
    return;
  }

  const isWarden = state.userRole === "warden";
  panel.classList.toggle("hidden", !isWarden);
  if (!isWarden) {
    rows.innerHTML = "";
    if (count) {
      count.textContent = "";
      count.classList.add("hidden");
    }
    return;
  }

  const query = getOfflineSearchQuery();
  if (!query) {
    rows.innerHTML = "";
    if (count) {
      count.textContent = "";
      count.classList.add("hidden");
    }
    return;
  }

  const students = getFilteredOfflineStudents();
  if (count) {
    count.textContent = `${OFFLINE_RESULT_COUNT_LABEL} ${students.length}`;
    count.classList.remove("hidden");
  }

  if (students.length === 0) {
    rows.innerHTML = `<div class="offline-student-empty">${PHONE_LOG_FILTER_EMPTY_LABEL}</div>`;
    return;
  }

  rows.innerHTML = students
    .slice(0, 60)
    .map((student) => {
      const name = String(student.name || "");
      const safeName = escapeHtml(name);
      const status = getOfflineStudentStatus(name);
      const grade = status.grade ? `<span>${status.grade}${GRADE_SUFFIX_LABEL}</span>` : "";
      const room = status.room ? `<span>${escapeHtml(status.room)}${ROOM_SUFFIX_LABEL}</span>` : "";
      const phoneActionLabel = status.phoneSubmitted ? PHONE_BORROW_ACTION_LABEL : PHONE_RETURN_ACTION_LABEL;
      const phoneTimer = getPhoneTimerText(status.phoneTakenAt, status.phoneSubmitted);
      const outingActionLabel = status.outingActive ? OUTING_RETURN_ACTION_LABEL : OUTING_LEAVE_ACTION_LABEL;
      const outingTimer = getOutingTimerText(status.outingStartedAt, status.outingActive);
      const phoneClass = status.phoneSubmitted ? "is-submitted" : "is-taken";
      const outingClass = status.outingActive ? "is-taken" : "is-submitted";
      const phoneDisabled = status.isOvernight || (status.phoneSubmitted && isPhoneConfiscated(status.phoneConfiscation)) ? " disabled" : "";
      const outingDisabled = status.isOvernight ? " disabled" : "";
      const phoneDisabledClass = phoneDisabled ? " is-disabled" : "";
      const outingDisabledClass = outingDisabled ? " is-disabled" : "";
      const statusLine = PHONE_BORROWING_ENABLED
        ? `${PHONE_STATUS_TITLE}: ${escapeHtml(phoneTimer)} / ${OUTING_STATUS_TITLE}: ${escapeHtml(outingTimer)}`
        : `${OUTING_STATUS_TITLE}: ${escapeHtml(outingTimer)}`;
      const phoneActionButton = PHONE_BORROWING_ENABLED
        ? `<button class="offline-status-button ${phoneClass}${phoneDisabledClass}" type="button" data-offline-phone-name="${safeName}"${phoneDisabled}>${phoneActionLabel}</button>`
        : "";

      return `
        <article class="offline-student-card">
          <div class="offline-student-main">
            <div class="offline-student-name-row">
              <strong>${safeName}</strong>
              ${grade}
              ${room}
            </div>
            <div class="offline-student-status-line">${statusLine}</div>
          </div>
          <div class="offline-status-actions">
            ${phoneActionButton}
            <button class="offline-status-button ${outingClass}${outingDisabledClass}" type="button" data-offline-outing-name="${safeName}"${outingDisabled}>${outingActionLabel}</button>
          </div>
        </article>
      `;
    })
    .join("");
}

function handleOfflineChangeInput(event) {
  if (event.target?.id !== "offlineStudentSearchInput") {
    return;
  }

  renderOfflineChangePanel();
}

async function handleOfflineChangeClick(event) {
  const phoneButton = PHONE_BORROWING_ENABLED ? event.target.closest("[data-offline-phone-name]") : null;
  const outingButton = event.target.closest("[data-offline-outing-name]");
  if (!phoneButton && !outingButton) {
    return;
  }

  if (state.userRole !== "warden") {
    return;
  }

  const button = phoneButton || outingButton;
  const name = String(phoneButton?.dataset.offlinePhoneName || outingButton?.dataset.offlineOutingName || "").trim();
  if (!name) {
    return;
  }

  const status = getOfflineStudentStatus(name);
  const reason = phoneButton
    ? status.phoneSubmitted
      ? await requestStatusChangeReason("phone")
      : ""
    : !status.outingActive
      ? await requestStatusChangeReason("outing")
      : "";
  if (reason === null) {
    return;
  }

  button.disabled = true;

  try {
    if (phoneButton) {
      await savePhoneStatusForName(name, !status.phoneSubmitted, reason);
    } else {
      await saveOutingStatusForName(name, !status.outingActive, reason);
    }
  } catch (error) {
    setMessage(error.message, true);
  } finally {
    renderOfflineChangePanel();
  }
}

